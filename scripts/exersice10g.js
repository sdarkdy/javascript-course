     function gam(){const buttonElement = document.querySelector(
      '.jc-gam');

      if (!buttonElement.classList.contains('is-toggled')) {
        buttonElement.classList.add('is-toggled');
      } else {
        buttonElement.classList.remove('is-toggled')
      }
  }
  
